#Ejemplos de varias formas de descomprimir

#- Descomprimir .tgz
$ tar -xvzf archivo.tgz
#- Descomprimir .tar
$ tar -xvf archivo.tar
#- Descomprimir .bz2
$ bzip2 -d fichero.bz2
#- Descomprimir .tar.bz2
$ tar jxvf archivo.tar.bz2
#- Descomprimir .gz
$ gzip -d fichero.gz
#- Descomprimir .tar.gz
$ tar zxf archivo.tar.gz
#- Descomprimir .rar
$ rar x archivo.rar
#- Descomprimir .lha
$ lha x archivo.lha
#- Descomprimir .zip
$ unzip archivo.zip
#- Descomprimir .arj
$ unarj archivo.arj o $ arj x archivo.arj
#- Descomprimir .zoo
$ zoo x archivo.zoo
